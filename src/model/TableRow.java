public class TableRow {

    private String group, firstName, surname, address,
    baptism, birthday, kapisanan;

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getBaptism() {
        return baptism;
    }

    public void setBaptism(String baptism) {
        this.baptism = baptism;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getKapisanan() {
        return kapisanan;
    }

    public void setKapisanan(String kapisanan) {
        this.kapisanan = kapisanan;
    }
}
